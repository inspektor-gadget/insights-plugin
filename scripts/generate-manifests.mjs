/**
 * Build-time script: runs `helm template` on the Inspektor Gadget chart and
 * generates a TypeScript module with the rendered manifests as typed constants.
 *
 * Usage: node scripts/generate-manifests.mjs [--version <chartVersion>]
 * Requires: helm CLI
 */
import { execSync } from 'child_process';
import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const CHART_REPO = 'gadget';
const CHART_REPO_URL = 'https://inspektor-gadget.github.io/charts';
const CHART_NAME = 'gadget/gadget';
const DEFAULT_NAMESPACE = 'gadget';
const RELEASE_NAME = 'gadget';

// Parse --version flag
const versionIdx = process.argv.indexOf('--version');
const chartVersion = versionIdx !== -1 ? process.argv[versionIdx + 1] : '';

function run(cmd) {
  return execSync(cmd, { encoding: 'utf-8', timeout: 120_000 }).trim();
}

// Ensure repo is added
try {
  run(`helm repo add ${CHART_REPO} ${CHART_REPO_URL} 2>/dev/null`);
} catch {
  // Already added
}
run(`helm repo update ${CHART_REPO}`);

// Build helm template command
let helmCmd = `helm template ${RELEASE_NAME} ${CHART_NAME} --namespace ${DEFAULT_NAMESPACE} --include-crds`;
if (chartVersion) {
  helmCmd += ` --version ${chartVersion}`;
}

console.log(`Running: ${helmCmd}`);
const rawYaml = run(helmCmd);

// Get chart version from metadata
let showCmd = `helm show chart ${CHART_NAME}`;
if (chartVersion) {
  showCmd += ` --version ${chartVersion}`;
}
const chartMeta = run(showCmd);
const versionMatch = chartMeta.match(/^appVersion:\s*(.+)$/m);
const appVersion = versionMatch ? versionMatch[1].replace(/['"]/g, '') : 'unknown';
const chartVersionMatch = chartMeta.match(/^version:\s*(.+)$/m);
const resolvedChartVersion = chartVersionMatch ? chartVersionMatch[1].trim() : 'unknown';

// Split multi-document YAML into individual documents
const documents = rawYaml
  .split(/^---\s*$/m)
  .map(d => d.trim())
  .filter(d => d.length > 0)
  // Strip leading comment lines (# Source: ...) but keep the YAML content
  .map(d => d.replace(/^(#[^\n]*\n)+/, '').trim())
  .filter(d => d.length > 0);

const manifests = [];

for (const doc of documents) {
  const kindMatch = doc.match(/^kind:\s*(.+)$/m);
  const apiVersionMatch = doc.match(/^apiVersion:\s*(.+)$/m);
  const nameMatch = doc.match(/^\s+name:\s*(.+)$/m);
  const nsMatch = doc.match(/^\s+namespace:\s*(.+)$/m);

  if (!kindMatch || !apiVersionMatch || !nameMatch) {
    console.warn('Skipping unparseable document');
    continue;
  }

  manifests.push({
    kind: kindMatch[1].trim(),
    apiVersion: apiVersionMatch[1].trim(),
    name: nameMatch[1].trim(),
    namespace: nsMatch ? nsMatch[1].trim() : undefined,
    yaml: doc,
  });
}

console.log(`Parsed ${manifests.length} resources:`);
for (const m of manifests) {
  console.log(`  ${m.kind}/${m.name} (${m.apiVersion})`);
}

// Generate TypeScript source
const outputDir = resolve(__dirname, '..', 'src', 'deploy');
mkdirSync(outputDir, { recursive: true });
const outputPath = resolve(outputDir, 'manifests.ts');

const tsSource = `/**
 * Auto-generated by scripts/generate-manifests.mjs
 * Chart: ${CHART_NAME} v${resolvedChartVersion} (appVersion: ${appVersion})
 * Generated: ${new Date().toISOString()}
 *
 * DO NOT EDIT — re-run \`npm run generate-manifests\` to update.
 */

export const CHART_VERSION = ${JSON.stringify(resolvedChartVersion)};
export const APP_VERSION = ${JSON.stringify(appVersion)};

export interface ManifestEntry {
  kind: string;
  apiVersion: string;
  name: string;
  namespace?: string;
  yaml: string;
}

/**
 * Pre-rendered Kubernetes manifests from the Inspektor Gadget Helm chart.
 * Each entry contains the raw YAML for one resource.
 */
export const MANIFESTS: ManifestEntry[] = ${JSON.stringify(manifests, null, 2)};

/**
 * Resource kinds in the order they should be applied.
 */
export const APPLY_ORDER: string[] = [
  'Namespace',
  'CustomResourceDefinition',
  'ServiceAccount',
  'ClusterRole',
  'ClusterRoleBinding',
  'Role',
  'RoleBinding',
  'ConfigMap',
  'Service',
  'DaemonSet',
  'Deployment',
];

/**
 * Resource kinds in the order they should be deleted (reverse of apply).
 */
export const DELETE_ORDER: string[] = [...APPLY_ORDER].reverse();
`;

writeFileSync(outputPath, tsSource, 'utf-8');
console.log(`\nWritten to ${outputPath}`);
